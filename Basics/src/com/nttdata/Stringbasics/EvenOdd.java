package com.nttdata.Stringbasics;

public class EvenOdd 
{
	public static void main(String[] args) 
	{
		for(int i=1;i<=100;i++)
		{
		   if(i%2==0)   
		    {
			   System.out.println(i+" :is a even number");
		    } 
		   else
		   {
			   System.out.println(i+" :is odd number");
		   }
		}
	
		
	}

}
