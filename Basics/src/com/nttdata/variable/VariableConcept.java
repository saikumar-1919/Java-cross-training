package com.nttdata.variable;

public class VariableConcept 
{
	public static void main(String[] args) 
	{
		int intValue = Integer.MAX_VALUE;
		byte byteValue = Byte.MAX_VALUE;
		float floatValue = Float.MAX_VALUE;
		short shortValue = Short.MAX_VALUE;
		long longValue = Long.MAX_VALUE;
		double doubleValue = Double.MAX_VALUE;
		boolean booleanValue = Boolean.FALSE;
		char c = 'S';
		System.out.println("value of integer is "+ intValue);
		System.out.println("value of byte  is "+ byteValue);
		System.out.println("value of float is "+ floatValue);
		System.out.println("value of short is "+ shortValue);
		System.out.println("value of long is "+ longValue);
		System.out.println("value of double is "+ doubleValue);
		System.out.println("value of boolean is "+ booleanValue);
		System.out.println("value of character is "+ c);
				
		
	}

}
